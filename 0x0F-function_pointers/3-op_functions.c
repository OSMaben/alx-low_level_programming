#include "3-calc.h"

/**
 * op_add - return the sum a + b
 *
 * @a: first argument
 * @b: second argument
 * * Return the sum a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return the difference a - b
 *
 * @a: first argument
 * @b: second argument
 * Return the difference a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return the product a * b
 *
 * @a: first argument
 * @b: second argument
 *
 * Return the difference a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return the quotient a / b
 *
 * @a: first argument
 * @b: second argument
 *
 * Return the result of the multiplication
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the remainder a % b
 *
 * @a: first argument
 * @b: second argument
 *
 * Return the result after modulo
 */

int op_mod(int a, int b)
{
	return (a % b);
}
